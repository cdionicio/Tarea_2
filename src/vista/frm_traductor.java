    private int contarRepeticiones(String texto, String letraMayuscula, String letraMinuscula, String letraTilde) {
        int contador = 0;
        for (int i = 0; i< texto.length(); i++) {
            char letra = texto.charAt(i);
            if (letra == letraMayuscula.charAt(0) || letra == letraMinuscula.charAt(0) || letra == letraTilde.charAt(0)) {
                contador++;
            }
        }
        return contador;
    }
    
    private String obtenerPalabrasPar(String[] palabras) {
        StringBuilder palabrasPar = new StringBuilder();
        for (String palabra : palabras) {
            if (palabra.length() % 2 == 0) {
                palabrasPar.append(palabra).append(" ");
            }
        }
        return palabrasPar.toString().trim();
    }

    private String obtenerPalabrasImpar(String[] palabras) {
        StringBuilder palabrasImpar = new StringBuilder();
        for (String palabra : palabras) {
            if (palabra.length() % 2 != 0) {
                palabrasImpar.append(palabra).append(" ");
            }
        }
        return palabrasImpar.toString().trim();
    }
