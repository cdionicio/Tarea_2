    private void btn_procesarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_procesarActionPerformed
        // TODO add your handling code here:
        String texto = txt_texto.getText();
        if (texto.isEmpty()) {
            JOptionPane.showMessageDialog(this, "El texto esta vacio", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        int longitudTexto = texto.length();
        txt_longitud.setText(String.valueOf(longitudTexto));
        
        String[] palabras = texto.split("\\s+");
        int totalPalabras = palabras.length;
        txt_totalPalabras.setText(String.valueOf(totalPalabras));
        
        String primeraLetra = String.valueOf(texto.charAt(0));
        txt_primeraLetra.setText(primeraLetra);
        
        String ultimaLetra = String.valueOf(texto.charAt(texto.length() - 1));
        txt_ultimaLetra.setText(ultimaLetra);
        
        int letraCentralIndex = longitudTexto / 2;
        String letraCentral = String.valueOf(texto.charAt(letraCentralIndex));
        txt_letraCentral.setText(letraCentral);
        
        String primeraPalabra = palabras[0];
        txt_primeraPalabra.setText(primeraPalabra);
        
        int palabraCentralIndex = totalPalabras / 2;
        String palabraCentral = palabras[palabraCentralIndex];
        txt_palabraCentral.setText(palabraCentral);
        
        String ultimaPalabra = palabras[totalPalabras - 1];
        txt_ultimaPalabra.setText(ultimaPalabra);
        
        int repeticionesA = contarRepeticiones(texto, "A", "a", "á");
        txt_repeticionesA.setText(String.valueOf(repeticionesA));

        int repeticionesE = contarRepeticiones(texto, "E", "e", "é");
        txt_repeticionesE.setText(String.valueOf(repeticionesE));

        int repeticionesI = contarRepeticiones(texto, "I", "i", "í");
        txt_repeticionesI.setText(String.valueOf(repeticionesI));

        int repeticionesO = contarRepeticiones(texto, "O", "o", "ó");
        txt_repeticionesO.setText(String.valueOf(repeticionesO));

        int repeticionesU = contarRepeticiones(texto, "U", "u", "ú");
        txt_repeticionesU.setText(String.valueOf(repeticionesU));
        
        String palabrasPar = obtenerPalabrasPar(palabras);
        txt_palabrasPar.setText(palabrasPar);

        String palabrasImpar = obtenerPalabrasImpar(palabras);
        txt_palabrasImpar.setText(palabrasImpar);
        
        String traduccion = traducirMurcielago(texto);
        txt_traduccion.setText(traduccion);
    }//GEN-LAST:event_btn_procesarActionPerformed

    private String traducirMurcielago(String texto) {
        String traduccion = "";
        for (char c : texto.toCharArray()) {
            int codigoAscci = (int) c;
            traduccion += String.valueOf(codigoAscci) + " ";
        }
        return traduccion.trim();
    }
            
